#include "util.h"

void delay(int delay)
{
	int count;
	for (count = 0; count < delay; count++)
	{
		unsigned short int i = 0;
		while (++i != 0)
		{
			unsigned char j = 0;
			while (++j != 0)
			{
			}
		}
	}
}
